/**
 * WhatsApp SDK Types
 *
 * Type definitions for WhatsApp API integration.
 */

/**
 * @deprecated WhatsApp credentials are no longer exposed.
 * Credentials are now managed internally by credentials-service.
 * SDK only receives temporary access tokens via getWhatsAppToken().
 */
export interface WhatsAppCredentials {
  apiUrl: string;
  clientId?: string; // Deprecated - no longer returned
  clientSecret?: string; // Deprecated - no longer returned
}

/**
 * OAuth token response
 */
export interface WhatsAppTokenResponse {
  access_token: string;
  token_type: string;
  expires_in: number;
}

/**
 * Media upload response
 */
export interface MediaUploadResponse {
  success: boolean;
  mediaHandle?: string;
  error?: string;
}

/**
 * Template component types
 */
export interface TemplateComponent {
  type: 'HEADER' | 'BODY' | 'FOOTER' | 'BUTTONS';
  format?: 'TEXT' | 'IMAGE' | 'VIDEO' | 'DOCUMENT';
  text?: string;
  example?: {
    header_handle?: string[];
    body_text?: string[][];
  };
  buttons?: Array<{
    type: string;
    text: string;
    url?: string;
  }>;
}

/**
 * Create template request
 */
export interface CreateTemplateRequest {
  name: string;
  language: string;
  category: 'MARKETING' | 'UTILITY' | 'AUTHENTICATION';
  components: TemplateComponent[];
}

/**
 * Create template response
 */
export interface CreateTemplateResponse {
  success: boolean;
  templateId?: string;
  status?: string;
  error?: string;
}

/**
 * Template status response
 */
export interface TemplateStatusResponse {
  success: boolean;
  status?: 'PENDING' | 'APPROVED' | 'REJECTED';
  name?: string;
  category?: string;
  error?: string;
}

/**
 * Send template message request
 */
export interface SendTemplateMessageRequest {
  templateId: string;
  phoneNumber: string;
  imageUrl?: string;
  metadata?: Record<string, unknown>;
  bodyParameters?: string[];
}

/**
 * Carousel card for carousel messages
 */
export interface CarouselCard {
  imageUrl: string;
  bodyParameters?: string[];
}

/**
 * Send carousel message request
 */
export interface SendCarouselMessageRequest {
  templateId: string;
  phoneNumber: string;
  cards: CarouselCard[];
  metadata?: Record<string, unknown>;
  mainBodyParameters?: string[];
  cardBodyParameters?: string[][];
}

/**
 * Send message response
 */
export interface SendMessageResponse {
  success: boolean;
  messageId?: string;
  error?: string;
}

/**
 * Config check result
 */
export interface ConfigCheckResult {
  configured: boolean;
  apiUrl: string;
}
